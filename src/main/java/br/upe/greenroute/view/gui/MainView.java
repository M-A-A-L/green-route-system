    @Override
    public void updateVehicleTable(List<VehicleModel> vehicles) {
        String[] columns = {"ID", "Tipo", "Modelo", "Autonomia (MAX)", "Carga Atual", "Consumo (Kwh/Km)", "Tempo de Recarga", "Conector", "Tempo de Recarga (Alta Potência)", "Capacidade do tanque (l)", "Consumo de Combustível", "Tipo de Combustível"};
        DefaultTableModel model = new DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        for (VehicleModel vehicle : vehicles) {
            String type = "";
            String connectorType = "-";
            String fastCharging = "-";
            String fuelTankCapacity = "-";
            String fuelConsumption = "-";
            String fuelType = "-";
            if (vehicle instanceof ElectricVehicleModel ev) {
                type = "Elétrico";
                connectorType = ev.getConnectorType();
                fastCharging = String.valueOf(ev.getFastCharging());
            } else if (vehicle instanceof HybridVehicleModel hv) {
                type = "Híbrido";
                fuelTankCapacity = String.valueOf(hv.getFuelTankCapacity());
                fuelConsumption = String.valueOf(hv.getFuelConsumption());
                fuelType = hv.getFuelType();
            }
            Object[] lines = {
                    vehicle.getId(),
                    type,
                    vehicle.getModel(),
                    vehicle.getMaximumAutonomy(),
                    vehicle.getCurrentBatteryCharge(),
                    vehicle.getConsumeKwhPerKm(),
                    vehicle.getFullRechargeTime(),
                    connectorType, fastCharging, fuelTankCapacity, fuelConsumption, fuelType
            };
            model.addRow(lines);
        }
        vehicleTable.setModel(model);
    }

